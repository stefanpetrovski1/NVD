// Дадена е рубиковата коцка, таа има n линии кои ја делат секоја страна од коцката.
// Креирајте функција која како влез прима n - број на линии на една страна на коцката,
// а како излез го дава вкупниот број на квадрати од кои се состои рубиковата коцка.
// Пример како треба да изгледа функцијата:

// howManyStickers(1) ➞ 6
// howManyStickers(2) ➞ 24

// howManyStickers(3) ➞ 54

function howManyStickers(lines) {
  return lines * lines * 6
}
